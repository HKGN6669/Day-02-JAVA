import java.util.*;
public class relational{
    public static void main(String[] args){
        int a=12;
        int b=5;
        Scanner sc = new Scanner(System.in);
        a=sc.nextInt();
        b=sc.nextInt();
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<+b);
        System.out.println(a<b);
        System.out.println(a>b);
    }
}